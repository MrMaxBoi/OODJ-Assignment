private void setupImageScaling() {
    // Load image once
    originalImage = new ImageIcon(getClass().getResource("/com/raven/icon/logoTransMed.png")).getImage();

    // Resize on label resize
    jLabel1.addComponentListener(new java.awt.event.ComponentAdapter() {
        @Override
        public void componentResized(java.awt.event.ComponentEvent e) {
            int width = jLabel1.getWidth();
            int height = jLabel1.getHeight();

            if (width > 0 && height > 0) {
                Image scaledImage = originalImage.getScaledInstance(width, height, Image.SCALE_SMOOTH);
                jLabel1.setIcon(new ImageIcon(scaledImage));
            }
        }
    });

    // Let JLabel resize properly
    jLabel1.setHorizontalAlignment(SwingConstants.CENTER);
    jLabel1.setVerticalAlignment(SwingConstants.CENTER);
    jLabel1.setPreferredSize(null);
    jLabel1.setMinimumSize(new Dimension(0, 0));
}


