    private void enterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonActionPerformed
    String enteredID = workIDTextField.getText().trim();
    String enteredPassword = new String(passwordTextField.getPassword()).trim();
    
    try {
        User user = UserRepository.findUser(enteredID);
        
        if (user != null && user.getPassword().equals(enteredPassword)) {
            String role = user.getRole();
            
            if (role.equalsIgnoreCase("Sales Manager")) {
                SM_Page dashboard = new SM_Page(user.getWorkID());
                dashboard.getHeader().setUserInfo(user.getName(), user.getRole());
                dashboard.setVisible(true);
            } 
            else if (role.equalsIgnoreCase("Finance Manager")) {
                FM_Page dashboard = new FM_Page(user.getWorkID());
                dashboard.getHeader().setUserInfo(user.getName(), user.getRole());
                dashboard.setVisible(true);
            }
            
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid ID or Password", "Login Failed", JOptionPane.ERROR_MESSAGE);
        }
    } catch (IOException ex) {
        JOptionPane.showMessageDialog(this, "Error accessing user data", "System Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_enterButtonActionPerformed

