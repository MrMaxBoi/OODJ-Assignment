    public void initButtons(String[] buttonNames, String[] icons, TableActionEvent event, int row) {
        removeAll(); // Clear existing buttons
        
        for (int i = 0; i < buttonNames.length; i++) {
            ActionButton btn = new ActionButton();
            btn.setIcon(new javax.swing.ImageIcon(getClass().getResource(icons[i])));
            btn.setActionCommand(buttonNames[i]);
            
            btn.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent ae) {
                    event.onAction(row, ae.getActionCommand());
                }
            });
            
            buttons.put(buttonNames[i], btn);
            add(btn);
        }
    }

    public JButton getButton(String name) {
        return buttons.get(name);
    }

